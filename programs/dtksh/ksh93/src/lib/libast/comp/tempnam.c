/*
 * tempnam implementation
 */

#include <ast_std.h>

#ifdef tempnam
#define _def_tempnam	1
#else
#define tempnam		______tempnam
#endif

#include <ast.h>
#include <stdio.h>

#if !_def_tempnam
#undef	tempnam
#endif

extern char*
tempnam(const char* dir, const char* pfx)
{
	return pathtmp(NiL, dir, pfx, NiL);
}
